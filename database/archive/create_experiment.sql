create table rws_experiment.result_table(
    result_id serial primary key,
    experiment_id int not null,
    result_datetime timestamp not null,
    model text not null,
    parameters jsonb not null,
    accuracy_train float not null,
    precision_train float not null,
    recall_train float not null,
    accuracy_val float []  not null,
    precision_val float [] not null,
    recall_val float [] not null,
    mean_tempval_accuracy float not null,
    mean_tempval_precision float not null,
    mean_tempval_recall float not null,
    auc_roc float [] not null,
    roc jsonb [] not null,
    pickle_path text not null,
    remarks text not null );
 
create table rws_experiment.experiment_table(
    experiment_id serial primary key,
    experiment_datetime timestamp,
    time_granularity varchar not null,
    segmentation_table varchar not null,
    time_lag_minutes int not null,
    weather_station_mapping_table varchar not null,
    sample_train boolean not null,
    sample_val boolean not null,
    sample_test boolean not null,
    train_sample_method_params jsonb not null,
    val_sample_method_params jsonb not null,
    test_sample_method_params jsonb not null,
    features_knmi_table varchar not null,
    features_hectopunten_table varchar not null,
    features_flow_table varchar not null,
    features_temporal_table varchar not null,
    features_knmi_list text [] not null,
    features_hectopunten_list text [] not null,
    features_flow_list text [] not null,
    features_temporal_list text [] not null,
    categorical_features_list text [] not null,
    numerical_features_list text [] not null,
    label_start timestamp  not null,
    label_end timestamp  not null,
    test_label_start timestamp not null,
    test_label_end timestamp not null,
    classifiers jsonb not null,
    grid_parameters jsonb not null,
    random_seed int not null,
    exp_remarks text not null);
